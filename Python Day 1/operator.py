
#Arithmetic Operator
Abhimanyu = 10
Bhairava = 3

print(Abhimanyu + Bhairava)     #Addition
print(Abhimanyu - Bhairava)     #Subtraction
print(Abhimanyu * Bhairava)     #Multiplication
print(Abhimanyu ** Bhairava)    # 1000 Power
print(Abhimanyu / Bhairava)     #Division
print(Abhimanyu // Bhairava)    #Floor division
print(Abhimanyu % Bhairava)     #Remainder

#Comparison Operator
x = 5
y = 10

print(x == y) 
print(x != y)
print(x > y)
print(x < y)
print(x >= y)
print(x <= y)

# Logical operator
age = 20
has_id = True

print(age > 18 and has_id)
print(age > 25 or has_id)
print(not has_id)


# Assignment Operator

